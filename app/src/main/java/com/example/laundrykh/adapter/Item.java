package com.example.laundrykh.adapter;
public class Item {
    private int img_clothes;
    private String num_clothes;
    private String name_clothes;

    public int getImg_clothes() {
        return img_clothes;
    }

    public void setImg_clothes(int img_clothes) {
        this.img_clothes = img_clothes;
    }

    public String getNum_clothes() {
        return num_clothes;
    }

    public void setNum_clothes(String num_clothes) {
        this.num_clothes = num_clothes;
    }

    public String getName_clothes() {
        return name_clothes;
    }

    public void setName_clothes(String name_clothes) {
        this.name_clothes = name_clothes;
    }
}
